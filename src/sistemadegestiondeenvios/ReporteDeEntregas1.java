    private void guardarEnArchivo(String nombreArchivo, String info) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(nombreArchivo))) {
            writer.write(info);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el archivo " + nombreArchivo, "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

