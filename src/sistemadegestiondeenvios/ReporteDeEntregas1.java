    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try (BufferedReader br = new BufferedReader(new FileReader("Seguimiento del Paquete.txt"))) {
            String linea;
            int cont1 = 0;
            int cont2 = 0;
            int cont3 = 0;
            int cont4 = 0;
            int cont5 = 0;
            int cont6 = 0;

            StringBuilder textoAtiempo = new StringBuilder();
            StringBuilder textoRetrasados = new StringBuilder();
            StringBuilder textoProblemas = new StringBuilder();
            StringBuilder textoEnRuta= new StringBuilder();
            StringBuilder textoCiudadOrigen = new StringBuilder();
            StringBuilder textoCiudadDestino = new StringBuilder();

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(",");
                if (datos.length >= 8) {
                    String nombre = datos[0].trim();
                    String codigoSeguimiento = datos[6].trim();
                    String estado = datos[7].trim();

                    String info = "• Nombre: " + nombre + "\n  Código: " + codigoSeguimiento + "\n  Estado: " + estado + "\n\n";

                    switch (estado) {
                        case "Entregado":
                            cont1++;
                            textoAtiempo.append(info);
                            break;
                        case "Retrasado":
                            cont2++;
                            textoRetrasados.append(info);
                            break;
                        case "En problema con el pedido":
                            cont3++;
                            textoProblemas.append(info);
                            break;
                        case "En ruta":
                            cont4++;
                            textoEnRuta.append(info);
                            break;
                        case "En ciudad de origen":
                            cont5++;
                            textoCiudadOrigen.append(info);
                            break;
                        case "En ciudad de destino":
                            cont6++;
                            textoCiudadDestino.append(info);
                            break;
                    
                    }
                }
            }

            // Mostrar los totales en los campos correspondientes
            tiempoTxt.setText(String.valueOf(cont1));
            retrasadosTxt.setText(String.valueOf(cont2));
            problemasTxt.setText(String.valueOf(cont3));
            rutaTxt.setText(String.valueOf(cont4));
            origenTxt.setText(String.valueOf(cont5));
            destinoTxt.setText(String.valueOf(cont6));

            // Mostrar los detalles en las áreas de texto
            jTextAreaAtiempo.setText(textoAtiempo.toString());
            jTextAreaRetrasados.setText(textoRetrasados.toString());
            jTextAreaEnProblemas.setText(textoProblemas.toString());
            jTextAreaOrigen.setText(textoCiudadOrigen.toString());
            jTextAreaDestino.setText(textoCiudadDestino.toString());
            jTextAreaRuta.setText(textoEnRuta.toString());
            
            guardarEnArchivo("Entregados.txt", textoAtiempo.toString());
            guardarEnArchivo("Retrasados.txt", textoRetrasados.toString());
            guardarEnArchivo("Problemas.txt", textoProblemas.toString());
            guardarEnArchivo("EnRuta.txt", textoEnRuta.toString());
            guardarEnArchivo("CiudadOrigen.txt", textoCiudadOrigen.toString());
            guardarEnArchivo("CiudadDestino.txt", textoCiudadDestino.toString());


        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo segui.txt", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

