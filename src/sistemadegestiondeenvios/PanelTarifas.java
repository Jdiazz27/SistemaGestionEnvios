    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        try {
            String codigoBuscado = seguiTxt.getText().trim();
            double peso = Double.parseDouble(tField.getText().trim());

            // ValidaciÃ³n del cÃ³digo de seguimiento numÃ©rico
            if (!codigoBuscado.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "Digite un valor nÃºmerico positivo en el cÃ³digo de seguimiento.", "Â¡Error!", JOptionPane.PLAIN_MESSAGE);
                seguiTxt.setText("");
                return;
            }

            // ValidaciÃ³n del peso
            if (peso < 1) {
                JOptionPane.showMessageDialog(this, "El peso debe ser mayor o igual a 1 kg.", "Â¡Error!", JOptionPane.PLAIN_MESSAGE);
                tField.setText("");
                return;
            }

            // Variables para almacenar la informaciÃ³n del cliente
            boolean encontrado = false;

            // BÃºsqueda en clientes.txt
            BufferedReader br = new BufferedReader(new FileReader("clientes.txt"));
            String linea;
             while ((linea = br.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes.length >= 7) {
                    String codigoCliente = partes[6].trim();
                    if (codigoCliente.equals(codigoBuscado)) {
                        encontrado = true;
                        break;
                    }
                }
            }
            br.close();

            if (!encontrado) {
                JOptionPane.showMessageDialog(this, "No se encontrÃ³ ningÃºn cliente con ese cÃ³digo de seguimiento.", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
                jTextArea1.setText("");
                return;
            }

            // Obtener destino y urgencia
            String destinoSeleccionado = destino.getSelectedItem().toString();
            String urgenciaSeleccionada = urgencia.getSelectedItem().toString();

            double tarifaDestino;
            switch (destinoSeleccionado) {
                case "Destino 1":
                    tarifaDestino = 500;
                    break;
                case "Destino 2":
                    tarifaDestino = 700;
                    break;
                case "Destino 3":
                    tarifaDestino = 900;
                    break;
                case "Destino 4":
                    tarifaDestino = 1100;
                    break;
                default:
                    tarifaDestino = 0;
                    break;
            }

            double multiplicadorUrgencia = urgenciaSeleccionada.equals("Urgente") ? 1.5 : 1.0;
            double total = peso * tarifaDestino * multiplicadorUrgencia;

            // Mostrar resultados en jTextArea1
            jTextArea1.setText("Costo total del envÃ­o: $" + String.format("%,.2f", total));

            // Guardar en tarifa.txt
            FileWriter fw = new FileWriter("tarifa.txt", true);
            fw.write(codigoBuscado + "," + destinoSeleccionado + "," + urgenciaSeleccionada + "," + peso + "," + total + "\n");
            fw.close();

            JOptionPane.showMessageDialog(this, "Â¡EnvÃ­o de paquete registrado correctamente!", "Ãxito", JOptionPane.INFORMATION_MESSAGE);

        } catch (NumberFormatException e) {
            tField.setText("");
            jTextArea1.setText("");
            JOptionPane.showMessageDialog(this, "DigitÃ³ un valor no vÃ¡lido. \nIngrese un nÃºmero mayor que 0.", "Â¡Error!", JOptionPane.PLAIN_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al procesar archivos: " + e.getMessage(), "Â¡Error!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_calcularActionPerformed

